function buildStateManagementGuidance(stack: DetectedStack): string | null {
    const sm = stack.stateManagement;
    if (sm.length === 0) { return null; }
    const lines: string[] = [];

    if (sm.includes('flutter_bloc') || sm.includes('bloc')) {
        lines.push('## State Management — flutter_bloc');
        lines.push('');
        lines.push('- BLoC and Cubit files live in `ui/blocs/<feature>/`.');
        lines.push('- Use `Cubit` for simple states, `Bloc` for event-driven flows.');
        lines.push('- Define named state classes (`AuthInitial`, `AuthLoading`, `AuthSuccess`, `AuthFailure`).');
        lines.push('- Never call business logic directly inside widgets — go through a Cubit/Bloc.');
        lines.push('- Use `BlocProvider` at the appropriate widget-tree level (route or subtree).');
        lines.push('');
    } else if (sm.includes('flutter_riverpod') || sm.includes('riverpod')) {
        lines.push('## State Management — Riverpod');
        lines.push('');
        lines.push('- Providers live in `ui/blocs/<feature>/` or a top-level `providers/` folder.');
        lines.push('- Prefer `AsyncNotifierProvider` for async state, `NotifierProvider` for sync.');
        lines.push('- Keep providers small and composable.');
        lines.push('- Annotate providers with `@riverpod` when using code generation.');
        lines.push('');
    } else if (sm.includes('provider')) {
        lines.push('## State Management — Provider');
        lines.push('');
        lines.push('- `ChangeNotifier` classes live in `ui/blocs/<feature>/`.');
        lines.push('- Register providers in your `MultiProvider` at the app root.');
        lines.push('');
    }

    return lines.length > 0 ? lines.join('\n') : null;
}

function buildDIGuidance(stack: DetectedStack): string | null {
    const di = stack.dependencyInjection;
    if (di.length === 0) { return null; }
    const lines: string[] = [];

    if (di.includes('get_it') || di.includes('injectable')) {
        lines.push('## Dependency Injection — GetIt');
        lines.push('');
        lines.push('- Register dependencies in a dedicated `injection.dart` (or `di/`) file.');
        lines.push('- Use factories for use cases, singletons for repositories and datasources.');
        if (di.includes('injectable')) {
            lines.push('- Annotate classes with `@injectable`, `@singleton`, or `@lazySingleton`.');
            lines.push('- Run `dart run build_runner build` after adding new annotated classes.');
        }
        lines.push('- Never call `GetIt.instance.get<T>()` inside domain classes.');
        lines.push('');
    }

    return lines.length > 0 ? lines.join('\n') : null;
}

function buildRoutingGuidance(stack: DetectedStack): string | null {
    const routing = stack.routing;
    if (routing.length === 0) { return null; }
    const lines: string[] = [];

    if (routing.includes('go_router')) {
        lines.push('## Routing — GoRouter');
        lines.push('');
        lines.push('- Define routes in a central `router.dart` file.');
        lines.push('- Use named routes (`GoRoute(name: ...)`) for type-safe navigation.');
        lines.push('- Screens receive their route parameters from `GoRouterState`, not from constructors.');
        lines.push('');
    } else if (routing.includes('auto_route')) {
        lines.push('## Routing — AutoRoute');
        lines.push('');
        lines.push('- Annotate screens with `@RoutePage()`.');
        lines.push('- Run `dart run build_runner build` after adding new routes.');
        lines.push('');
    }

    return lines.length > 0 ? lines.join('\n') : null;
}

function buildSerializationGuidance(stack: DetectedStack): string | null {
    const ser = stack.serialization;
    if (ser.length === 0) { return null; }
    const lines: string[] = [];

    if (ser.includes('freezed') || ser.includes('freezed_annotation')) {
        lines.push('## Models / Serialisation — Freezed');
        lines.push('');
        lines.push('- Annotate model classes with `@freezed`.');
        lines.push('- Run `dart run build_runner build` after modifying model classes.');
        lines.push('- Keep the generated `.freezed.dart` and `.g.dart` files — do not edit them manually.');
        lines.push('- Data models (DTOs) live in `data/models/`; domain entities in `domain/entities/`.');
        lines.push('');
    } else if (ser.includes('json_serializable') || ser.includes('json_annotation')) {
        lines.push('## Models / Serialisation — json_serializable');
        lines.push('');
        lines.push('- Annotate model classes with `@JsonSerializable()`.');
        lines.push('- Run `dart run build_runner build` after modifying model classes.');
        lines.push('- Add `fromJson` factory and `toJson` method to each model.');
        lines.push('');
    }

    return lines.length > 0 ? lines.join('\n') : null;
}
